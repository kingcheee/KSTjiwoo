"""
===========================================================
📌 가설검정 기초 — 스터디 발표용 치트시트
===========================================================
목표: 데이터로 주장의 유의미성을 검증하는 가설검정 과정 이해하기
시간: 발표 약 3분분량
===========================================================
"""

import numpy as np
import pandas as pd
from scipy import stats

# ==========================================================
# 1. 가설검정이 뭔데? — 통계적 재판 ⚖️
# ==========================================================
# 비유: 법정 재판
# - 피고인은 무죄가 기본 (귀무가설: 효과가 없다)
# - 검찰이 유죄를 증명해야 함 (대립가설: 효과가 있다)
# - 합리적 의심 없이 증명해야 유죄 판결 (p-value < 0.05)

print("""
╔══════════════════════════════════════════════════════════╗
║              ⚖️ 가설검정 = 통계적 재판                    ║
╠══════════════════════════════════════════════════════════╣
║                                                          ║
║  귀무가설 (H₀): 피고인은 무죄 (효과가 없다)              ║
║  대립가설 (H₁): 검찰의 주장 (효과가 있다)                ║
║                                                          ║
║  p-value: "무죄인데 이런 증거가 나올 확률"               ║
║  유의수준(α): 5% — 5% 미만이면 "유죄!" (귀무가설 기각)  ║
║                                                          ║
╚══════════════════════════════════════════════════════════╝
""")


# ==========================================================
# 2. 에너지 음료 예시 — 가설검정 6단계
# ==========================================================

# 📋 실험 설정:
# - 30명의 학생 중 15명에게 에너지 음료, 15명에게 플라시보
# - 같은 시험을 치르게 함
# - 에너지 음료가 정말 점수를 올려줄까?

np.random.seed(42)

# 가상의 시험 점수 데이터 생성
# 에너지 음료 그룹: 평균 78, 표준편차 10
energy_drink = np.random.normal(78, 10, 15).round(1)
# 플라시보 그룹: 평균 72, 표준편차 10
placebo = np.random.normal(72, 10, 15).round(1)

print("=== 에너지 음료 실험 데이터 ===")
print(f"에너지 음료 그룹: {energy_drink}")
print(f"플라시보 그룹:    {placebo}")
print(f"에너지 음료 평균: {energy_drink.mean():.1f}")
print(f"플라시보 평균:    {placebo.mean():.1f}")
print(f"평균 차이:        {energy_drink.mean() - placebo.mean():.1f}")
print()


# ==========================================================
# 3. 가설검정 6단계 진행
# ==========================================================

# ─── 1단계: 문제 정의 ───
print("📌 1단계: 문제 정의")
print("   '에너지 음료가 학생들의 시험 성적에 영향을 줄 것이다'")
print()

# ─── 2단계: 가설 설정 ───
print("📌 2단계: 가설 설정")
print("   귀무가설 (H₀): 에너지 음료를 마신 그룹과 플라시보 그룹의 평균 점수가 같다")
print("                  (μ₁ = μ₂)")
print("   대립가설 (H₁): 두 그룹의 평균 점수가 다르다")
print("                  (μ₁ ≠ μ₂)")
print()

# ─── 3단계: 표본 추출 & 데이터 수집 ───
print("📌 3단계: 표본 추출 & 데이터 수집")
print(f"   에너지 음료 그룹: n={len(energy_drink)}, 평균={energy_drink.mean():.1f}, 표준편차={energy_drink.std():.1f}")
print(f"   플라시보 그룹:    n={len(placebo)}, 평균={placebo.mean():.1f}, 표준편차={placebo.std():.1f}")
print()

# ─── 4단계: 검정 통계량 계산 ───
print("📌 4단계: 검정 통계량 계산")
# 독립표본 t-검정 (두 그룹의 평균 비교)
t_stat, p_value = stats.ttest_ind(energy_drink, placebo)
print(f"   t-통계량: {t_stat:.4f}")
print(f"   p-value:  {p_value:.4f}")
print()

# ─── 5단계: 유의성 검정 ───
print("📌 5단계: 유의성 검정")
alpha = 0.05  # 유의수준 5%
print(f"   유의수준 (α): {alpha}")
print(f"   p-value: {p_value:.4f}")

if p_value < alpha:
    print(f"   ✅ p-value ({p_value:.4f}) < α ({alpha})")
    print("   → 귀무가설을 기각한다!")
else:
    print(f"   ❌ p-value ({p_value:.4f}) ≥ α ({alpha})")
    print("   → 귀무가설을 기각할 충분한 증거가 없다.")
print()

# ─── 6단계: 결론 도출 ───
print("📌 6단계: 결론 도출")
if p_value < alpha:
    print("   🎯 결론: 에너지 음료가 학생들의 시험 성적에")
    print("      통계적으로 유의미한 영향을 미친다!")
else:
    print("   🎯 결론: 에너지 음료가 시험 성적에 유의미한 영향을")
    print("      미친다는 증거가 부족하다.")
print()


# ==========================================================
# 4. p-value 시각화
# ==========================================================
print("=== p-value 이해하기 ===")
print("""
   p-value = 0.05 기준선
   
   p < 0.05  → 🎯 유의미! (귀무가설 기각)
   p ≥ 0.05  → ❌ 유의미하지 않음 (귀무가설 유지)
   
   p-value가 작을수록 → 귀무가설이 참이기 어렵다는 증거가 강함!
""")

# 다양한 p-value 예시
p_values = [0.50, 0.10, 0.05, 0.01, 0.001]
print("   p-value 크기별 해석:")
for p in p_values:
    if p < 0.001:
        sig = "매우 유의미 (***)"
    elif p < 0.01:
        sig = "매우 유의미 (**)"
    elif p < 0.05:
        sig = "유의미 (*)"
    else:
        sig = "유의미하지 않음 (ns)"
    print(f"   p = {p:.3f} → {sig}")


# ==========================================================
# 5. 주요 가설검정 방법 — 상황별 선택 가이드
# ==========================================================
print("""
╔══════════════════════════════════════════════════════════╗
║           🔬 가설검정 방법 선택 가이드                    ║
╠══════════════════════════════════════════════════════════╣
║                                                          ║
║  1. 단일 표본 t-검정                                     ║
║     → 하나의 그룹 평균이 특정 값과 다른지                 ║
║     예: "우리 반 평균이 75점과 다른가?"                  ║
║     코드: stats.ttest_1samp(data, 75)                    ║
║                                                          ║
║  2. 독립표본 t-검정                                      ║
║     → 두 그룹의 평균이 다른지                            ║
║     예: "에너지 음료 vs 플라시보 그룹 평균 차이"         ║
║     코드: stats.ttest_ind(group1, group2)                ║
║                                                          ║
║  3. 대응표본 t-검정                                      ║
║     → 같은 그룹의 전후 비교                              ║
║     예: "다이어트 전후 체중 변화"                        ║
║     코드: stats.ttest_rel(before, after)                 ║
║                                                          ║
║  4. 카이제곱 검정                                        ║
║     → 범주형 변수 간의 관계                              ║
║     예: "성별과 제품 선호도가 관련이 있는가?"            ║
║     코드: stats.chi2_contingency(table)                  ║
║                                                          ║
║  5. 분산분석 (ANOVA)                                     ║
║     → 세 그룹 이상의 평균 비교                           ║
║     예: "3개 학원의 수강생 성적 차이"                    ║
║     코드: stats.f_oneway(group1, group2, group3)         ║
║                                                          ║
╚══════════════════════════════════════════════════════════╝
""")


# ==========================================================
# 6. 실전 예시 — 카이제곱 검정
# ==========================================================
print("=== 실전 예시: 카이제곱 검정 ===")
print("질문: '성별과 생존 여부는 관련이 있는가?'")
print()

# 가상의 분할표 (타이타닉 스타일)
#          생존  사망
# 여성      70    30
# 남성      30    70

observed = np.array([[70, 30], [30, 70]])
chi2, p, dof, expected = stats.chi2_contingency(observed)

print(f"관측값:")
print(f"         생존  사망")
print(f"  여성    {observed[0][0]:3d}   {observed[0][1]:3d}")
print(f"  남성    {observed[1][0]:3d}   {observed[1][1]:3d}")
print()
print(f"카이제곱 통계량: {chi2:.4f}")
print(f"p-value: {p:.6f}")
print(f"자유도: {dof}")
print()

if p < 0.05:
    print("✅ 성별과 생존 여부는 통계적으로 유의미한 관련이 있다!")
else:
    print("❌ 성별과 생존 여부는 유의미한 관련이 없다.")


# ==========================================================
# 7. 가설검정 시 주의사항
# ==========================================================
print("""
╔══════════════════════════════════════════════════════════╗
║              ⚠️ 가설검정 주의사항                         ║
╠══════════════════════════════════════════════════════════╣
║                                                          ║
║  1. p-value ≠ 효과의 크기                                ║
║     → 유의미하다고 해서 그 차이가 크다는 건 아님!        ║
║     → 큰 표본에서는 작은 차이도 유의미해질 수 있음      ║
║                                                          ║
║  2. 상관관계 ≠ 인과관계                                  ║
║     → "아이스크림 판매량과 익사 수가 관련있다"           ║
║     → 둘 다 여름에 늘어나는 거지, 아이스크림이 익사를    ║
║       일으키는 게 아님!                                  ║
║                                                          ║
║  3. 다중비교 문제                                        ║
║     → 여러 번 검정하면 우연히 유의미한 결과가 나올 수 있음║
║     → 본페로니 보정 등의 방법으로 해결                   ║
║                                                          ║
║  4. 가정 확인                                            ║
║     → t-검정: 정규성, 등분산성 가정 필요                 ║
║     → 가정이 안 맞으면 비모수 검정 사용                  ║
║                                                          ║
╚══════════════════════════════════════════════════════════╝
""")

print("✅ 가설검정 기초 치트시트 끝!")
