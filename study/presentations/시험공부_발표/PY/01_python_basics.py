# ==========================================================
# 1. print() 함수 — 출력 제어
# ==========================================================
print("안녕!")  # 기본 출력 (자동 줄바꿈)

# end 속성: 줄바꿈 없이 이어서 출력
print("Hello", end=" ")
print("World")  # Hello World

# sep 속성: 여러 값 사이에 구분자 넣기
print("사과", "바나나", "포도", sep=" → ")  # 사과 → 바나나 → 포도


# ==========================================================
# 2. f-string — 문자열 포매팅 (실무에서 가장 많이 쓰이는 방식!)
# ==========================================================
name = "규연"
age = 25
print(f"나의 이름은 {name} 입니다. 나이는 {age} 입니다.")
# 나의 이름은 규연 입니다. 나이는 25 입니다.

# f-string 안에서 연산도 가능!
print(f"10년 후 나이: {age + 10}")  # 10년 후 나이: 35


# ==========================================================
# 3. 조건문 — if, elif, else
# ==========================================================
# ⚠️ 중요: 들여쓰기(Indentation)가 매우 중요! 탭(Tab) 사용에 주의!
# ⚠️ 중요: 범위가 작은 것부터 시작해서 넓은 쪽으로 내려가며 작성!

score = 85

if score >= 90:
    grade = "A"
elif score >= 80:
    grade = "B"      # ← 여기 해당!
elif score >= 70:
    grade = "C"
else:
    grade = "F"

print(f"점수 {score}점 → {grade}등급")

# ❌ 잘못된 순서 (큰 범위부터 작성하면 아래 조건이 무시됨!)
# if score >= 70:    → 85 >= 70이 True라서 여기서 멈춤!
#     grade = "C"
# elif score >= 80:  → 절대 실행 안 됨!
#     grade = "B"

# in 연산자 활용
pocket = ["money", "phone", "keys"]
if "money" in pocket:
    print("택시를 타자!")  # 주머니에 돈이 있으면 True


# ==========================================================
# 4. 반복문 — while, for
# ==========================================================
# 강사님: "while보다 for문이 훨씬 더 중요하다!" 💪

# --- while문: 반복 횟수가 불명확할 때 ---
# 패턴: 초기식 → 조건식 → 실행문 → 증감식
# ⚠️ 증감식 없으면 무한 루프!
count = 1
while count <= 5:
    print(f"while {count}번째")
    count += 1  # 증감식 반드시 필요!

# --- for문: 코딩에서 가장 많이 쓰이는 반복문! ---
# range() 함수 연동
total = 0
for num in range(1, 101):  # 1부터 100까지
    total += num
print(f"1~100 합계: {total}")  # 5050

# 리스트 연동
fruits = ["사과", "바나나", "포도"]
for fruit in fruits:
    print(f"오늘의 과일: {fruit}")

# len()으로 횟수 지정
for i in range(len(fruits)):
    print(f"{i}번째 과일: {fruits[i]}")


# ==========================================================
# 5. 리스트 컴프리헨션 (List Comprehension) ⭐ 가장 중요!
# ==========================================================
# 강사님: "가장 중요하다고 거듭 강조된 핵심 문법!" 🔥
# 기존: 빈 리스트 만들고 → for문 돌리고 → append로 추가 (여러 줄)
# 컴프리헨션: 단 한 줄로 압축!

# 기존 방식 (3줄)
result = []
for x in range(1, 6):
    result.append(x * 3)
print(result)  # [3, 6, 9, 12, 15]

# 리스트 컴프리헨션 (1줄!) ⭐
result = [x * 3 for x in range(1, 6)]
print(result)  # [3, 6, 9, 12, 15]

# 구조: [표현식 for 변수 in 반복가능한객체]

# if 조건 추가 가능!
# 1~10 중 짝수만 제곱해서 새 리스트 만들기
even_squares = [x**2 for x in range(1, 11) if x % 2 == 0]
print(even_squares)  # [4, 16, 36, 64, 100]

# 실전 예시: 문자열 리스트에서 길이가 3 이상인 것만
words = ["hi", "hello", "hey", "world", "py"]
long_words = [w for w in words if len(w) >= 3]
print(long_words)  # ['hello', 'hey', 'world']


